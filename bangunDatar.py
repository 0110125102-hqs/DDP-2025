def persegi(sisi):
    return sisi * sisi
print(persegi(5))

def persegiPanjang(panjang, lebar):
    return panjang * lebar
print(persegiPanjang(10,5))

def segitiga(alas, tinggi):
    return 0.5 * alas * tinggi
print(segitiga(8,10))

def lingkaran(jari_jari):
    return jari_jari ** 2
print(lingkaran(4))

def jajarGanjar(alas, tinggi):
    return alas * tinggi
print(jajarGanjar(4,7))